function buildPrompt({role, goal, inputs, sheetColumns, figmaBoard, extra}) {
  return `You are helping me build an AI-first workflow.\n\nRole:\n${role}\n\nGoal:\n${goal}\n\nMy context:\n${inputs}\n\nUse web search when it helps uncover fresh sources, examples, or missing context.\n\nCreate the output in a way that I can immediately use.\n\nGoogle Sheets structure:\n${sheetColumns}\n\nFigma board structure:\n${figmaBoard}\n\nConstraints:\n- Keep the workflow practical.\n- Make tradeoffs visible.\n- Show me what to do next.\n- Use concise labels and structured sections.\n\nExtra instructions:\n${extra}`;
}

